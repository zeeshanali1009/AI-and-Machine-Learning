# datatype is the type of the variable like it represents the type so that the only that type of data is stored into the variables.
# Following are the different datatypes:
# Numeric Data (int, float, complex)
# String Data (str) 
# Boolean Data (bool)
# Sequential Data (list(mutable), tuple(not mutable))
# Map Data (dictionary)

# examples
# numeric datatype
int_number = 12
float_number = 12.12
complex_number = complex(12,12)

# string datatype 
string_data = "Zeeshan"

# boolean datatype 
isvalid   = True

# sequential datatype
list = [12,2,35,5,23,34, "Zeeshan", [643,34,35,46,5]]
tuple = ((12,23,34,45,46,56), "Zeeshan", (13,24.35,45.56,56))

# map datatype
this_dictionary = {"name" : "Zeeshan", "Age": 21, "Addresss": "Lahore"
}


